#include "../include/cub3d.h"

int	create_trgb(int r, int g, int b)
{
	if ((r < 0 || r > 255) || (g < 0 || g > 255) || (b < 0 || b > 255))
	{
		printf("Error : color error\n");
		exit(0);
	}
	return (r << 16 | g << 8 | b);
}
